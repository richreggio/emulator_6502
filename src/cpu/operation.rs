type InstructionFunction = fn(&mut Cpu, &mut Operation);

pub struct Operation {
    pub addressing_mode: AddressingMode,
    pub instruction_mnemonic: String,
    pub instruction_function: InstructionFunction,
    pub cycles: u8,
}

impl Operation {
    pub fn next(cpu: &mut Cpu) -> Operation {
        // Which operation is next to be executed
        let opcode = cpu.ram.read_byte(cpu.registers.program_counter);

        // What the operation addressing mode is, the function that perform it, and how many cycles it takes to complete
        let (addressing_mode, instruction_function, mnemonic, mut cycles) =
            resolve_opcode(opcode, cpu.registers.program_counter + 1);

        // Does the work of calculating the addressing mode's final value
        let (addressing_mode, additional_cycles) = addressing_mode.process(cpu);

        cycles += additional_cycles;

        cpu.registers.increment_program_counter(&addressing_mode);

        Operation {
            addressing_mode,
            instruction_mnemonic: String::from(mnemonic),
            instruction_function,
            cycles,
        }
    }

    pub fn disassemble(&self) {
        match self.addressing_mode {
            Implied => println!("Opcode: {}, Implied", self.instruction_mnemonic),
            Accumulator => println!("Opcode: {}, Accumulator", self.instruction_mnemonic),
            Immediate(address) => println!(
                "Opcode: {}, Immediate address: {:#06X}",
                self.instruction_mnemonic, address
            ),
            Absolute(address) => println!(
                "Opcode: {}, Absolute address: {:#06X}",
                self.instruction_mnemonic, address
            ),
            AbsoluteXIndex(address) => println!(
                "Opcode: {}, Absolute X Address: {:#06X}",
                self.instruction_mnemonic, address
            ),
            AbsoluteYIndex(address) => println!(
                "Opcode: {}, Absolute Y Address: {:#06X}",
                self.instruction_mnemonic, address
            ),
            AbsoluteIndirect(address) => println!(
                "Opcode: {}, Absolute Indirect Address: {:#06X}",
                self.instruction_mnemonic, address
            ),
            ZeroPage(address) => println!(
                "Opcode: {}, Zero Page Address: {:#06X}",
                self.instruction_mnemonic, address
            ),
            ZeroPageXIndex(address) => println!(
                "Opcode: {}, Zero Page X Address: {:#06X}",
                self.instruction_mnemonic, address
            ),
            ZeroPageYIndex(address) => println!(
                "Opcode: {}, Zero Page Y Address: {:#06X}",
                self.instruction_mnemonic, address
            ),
            ZeroPageXIndexIndirect(address) => println!(
                "Opcode: {}, Zero Page X Indirect Address: {:#06X}",
                self.instruction_mnemonic, address
            ),
            ZeroPageYIndexIndirect(address) => println!(
                "Opcode: {}, Zero Page Y Indirect Address: {:#06X}",
                self.instruction_mnemonic, address
            ),
            Relative(address) => println!(
                "Opcode: {}, Relative Address: {:#06X}",
                self.instruction_mnemonic, address
            ),
        }
    }
}

